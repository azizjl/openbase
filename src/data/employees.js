export function getEmployeeById(id) {
  return employees.find((e) => e.id === id)
}

export function getDepartmentById(id) {
  return departments.find((d) => d.id === id)
}

export function getDirectReports(managerId) {
  return employees.filter((e) => e.managerId === managerId)
}

export function getManager(employeeId) {
  const employee = getEmployeeById(employeeId)
  if (!employee?.managerId) return null
  return getEmployeeById(employee.managerId)
}
